CREATE DATABASE Proyecto_Practico_SQL

USE Proyecto_Practico_SQL

CREATE TABLE Proveedores (
    id_proveedor INT PRIMARY KEY,
    nombre_proveedor VARCHAR(40)
);

CREATE TABLE Producto (
    id_producto INT PRIMARY KEY,
    nombre_producto VARCHAR(40),
    tipo VARCHAR(40),
    cantidad INT,
    precio DECIMAL(9,2),
    id_proveedor INT FOREIGN KEY REFERENCES Proveedores(id_proveedor)
);

CREATE TABLE Clientes (
    id_cliente INT PRIMARY KEY,
    nombre_cliente VARCHAR(40),
    correo VARCHAR(40)
);


CREATE TABLE Ventas (
    id_venta INT PRIMARY KEY,
    fecha_venta DATE,
    id_cliente INT FOREIGN KEY REFERENCES Clientes(id_cliente),
    id_sucursal INT FOREIGN KEY REFERENCES Sucursal(id_sucursal)
);

CREATE TABLE Detalle_Venta (
    id_venta INT FOREIGN KEY REFERENCES Ventas(id_venta),
    id_producto INT FOREIGN KEY REFERENCES Producto(id_producto),
    Cantidad INT
    PRIMARY KEY (id_venta, id_producto)   
);

CREATE TABLE Sucursal (
    id_sucursal INT PRIMARY KEY,
    ubicacion VARCHAR(40)
);

CREATE TABLE Empleados (
    id_empleado INT PRIMARY KEY,
    nombre_empleado VARCHAR(40),
    fecha_ingreso DATE,
    id_sucursal INT FOREIGN KEY REFERENCES Sucursal(id_sucursal)    
);

CREATE TABLE Sistema (
    id_usuario INT PRIMARY KEY,
    fecha_creacion DATE,
    id_empleado INT FOREIGN KEY REFERENCES Empleados(id_empleado)    
);

