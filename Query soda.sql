 ---żCUAL ES EL PRECIO MAS BAJO ENTRE TODOS LOS PRODUCTOS? DEVUELVE EL PRECIO MAS BAJO REGISTRADO
 SELECT MIN(PRECIO) AS PRECIO_MINIMO
