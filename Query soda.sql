--13 OPERACIONES CON %

---CUAL ES EL PROCENTAJE DE PRODUCTOS POR TIPO
SELECT tipo, SUM(cantidad) AS total_por_tipo,
    SUM(cantidad) * 100 / (SELECT SUM(cantidad) FROM Producto) AS porcentaje
FROM Producto
GROUP BY tipo
ORDER BY porcentaje DESC;

--14 COUNT + GROUP BY + ORDER BY
--CUENTA REGISTROS POR GRUPO Y LOS ORDENA

---żCUANTAS COMPRAS HA HECHO CADA CLIENTE DE MAYOR A MENOR?

SELECT c.nombre_cliente, COUNT(v.ID_venta) AS total_ventas
FROM Clientes c
JOIN Ventas v ON c.id_cliente = v.id_cliente
GROUP BY c.nombre_cliente
ORDER BY total_ventas DESC;


--15 CONSULTA AGREGADA + HAVING
-- FILTRA GRUPOS QUE CUMPAL CONDICIONES CON FUNCIONES AGREGADAS
 ---żQUE CLIENTES HAN REALIZADO COMPRAS MAYORES O IGUALES A 4?

 SELECT c.nombre_cliente, COUNT(v.id_cliente) AS total_comprado
FROM Clientes c
JOIN Ventas v ON c.id_cliente = v.id_cliente
GROUP BY c.nombre_cliente
HAVING COUNT(v.id_cliente) >= 4 
ORDER BY total_comprado DESC