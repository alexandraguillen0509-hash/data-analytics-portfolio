----1.GROUP BY - AGRUPA LOS RESULTADOS POR  UNA O VARIAS COLUMNAS
 ---żCUANTAS UNIDADES HAY EN INVENTARIO POR TIPO DE PRODUCTO?
 SELECT TIPO ,SUM (CANTIDAD) AS TOTAL_INVENTARIO
 FROM PRODUCTO
 GROUP BY TIPO;

 ----2 ORDER BY - ORDENA LOS RESULTADOS POR UNA O MAS COLUMNAS, Se puede generar ASC O DESC 
---żCUAL ES LA VENTA MAS ANTIGUA?
 SELECT *
 FROM VENTAS
 ORDER BY FECHA_VENTA ASC;

 -----3. DISTINCT - DEVUELVE VALORES UNICOS EN UNA COLUMNA ELIMINANDO DUPLICADOS

---- żQUE TIPOS DE PRODUCTOS EXISTEN?
 SELECT DISTINCT TIPO
 FROM PRODUCTO;

---4 BETWEEN - FILTRA LOS RESULTADOS DENTRO DE UN RANGO
---żQUE VENTAS SE REALIZARON ENTRE DOS FECHAS ESPECIFICAS?
 SELECT * 
 FROM VENTAS 
 WHERE FECHA_VENTA BETWEEN '2022-01-01' AND '2022-01-31';

 ---5 AVG - CALCULA EL PROMEDIO DE LOS VALORES NUMERICOS DE UNA COLUMNA.
 ---żCUAL ES EL PRECIO PROMEDIO DE LOS PRODUCTOS?
 SELECT AVG(PRECIO) AS PRECIO_PROMEDIO
 FROM PRODUCTO

 ----6 COUNT - CUENTA CUANTOS REGISTROS EN LAS COLUMNAS 

----żCUANTOS CLIENTES HAY REGISTRADOS?
SELECT COUNT(*) AS TOTAL_CLIENTES
FROM CLIENTES;

----7 HAVING - FILTRA LOS RESULTADOS DESPUES DE USAR GROUP BY . SE USA CON FUNCIONES AGREGADADAS COMO SUM,AVG ETC

---żQUE CLIENTE HA REALIZADO MAS DE 3 COMPRAS?
 SELECT ID_CLIENTE , COUNT(*) AS TOTAL_COMPRAS
 FROM VENTAS
 GROUP BY ID_CLIENTE
 HAVING COUNT(*)> 3

